<h1>Toujours plus avec GOOGLE</h1>

<h2>Introduction</h2>

<p>Voilà un article dédié aux possibilités offertes par Google, le moteur de recherche.<br />
Vous l’utilisez peut être tout les jours, vous connaissez peut être certaines astuces basiques…voilà les autres !<br />
Les astuces vont par difficulté croissantes : cependant, il est fort probable que certaines astuces vous intéressent plus que d’autres…alors n’arrêtez pas après la cinquième astuce !</p>

<h2>Historique de Google</h2>
<p>Faut-il vraiment présenter Google ?<br />
Roi incontesté de la recherche depuis de nombreuses années, Google a été fondé par deux étudiants en 1998 : Larry Page et Sergey Brin, qui créent l'algorithme de recherche.<br />
Dix ans plus tard, Google représente 210 milliards de dollars et indexe plus de 8 milliars de pages Web.<br />
Pour l'anecdote, on citera la devise de la société : <cite>Dont be evil</cite>.</p>
<p>Mais ce n'est pas ce qui est important...le but de cet article est de découvrir Google plus en profondeur.</p>
<h2>Les requêtes</h2>

<h3>Exclure un terme</h3>

<p>Surement la fonction la plus connue !<br />
Si vous cherchez des informations sur le capitaine haddock par exemple, la requête haddock –poisson enlèvera toute référence au poisson. Je ne m’attarde pas sur cette fonction qui avouons-le, n’est pas très utile…</p>

<h3>Rechercher une suite de mots</h3>

<p>Vous recherchez une personne, mais la requête Robert Dupont renvoie uniquement des liens vers les enquêteurs Dupont de Tintin, ou vers un Robert connu ? Entourez votre requête de guillemets : « Robert Dupont » ne renverra que les résultats contenant les deux mots qui se suivent. On peut aussi utiliser Robert AND Dupont qui n’affichera que les pages contenant les deux mots en même temps. </p>

<h3>Conversion de devises</h3>

<p>Il suffit de marquer de matière très naturelle la conversion à faire : 100 $ en euros, ou 100 dollars en €... (Réponse : <cite class="comment">100 $ US = 67,8472081 euros</cite>). Les taux sont mis à jour en temps réel, et cela fonctionne pour la grande majorité des devises. </p>

<h3>Calcul</h3>
<p><img src="Images/calc.gif" alt="Calculatrice" />Google intègre une calculatrice convertisseur assez pratique. Que vous demandiez le calcul trivial (1256+126-456)*2, ou le calcul un peu plus complexe ? e^(i*pi) + 2^(1/2), la calculatrice vous rendra des services innombrables).<br />
(Réponse : <cite class="comment">(e^(i * pi)) + (2^(1 / 2)) = 0,414213562)</cite>. Surtout qu'elle est plus simple à utiliser que celle de Windows ! De plus, vous pouvez aussi convertir des unités (100 km en m), et la calculatrice de google.com (pas celle de google.fr malheureusement) permet même des opérations telles que 34 in binary ou 16548 bytes in kilobytes.</p>
<h4>En bonus</h4>
<p>@uguste fait remarquer que la calculatrice Google ne s'arrête pas là ! Elle répond même aux questions les plus complexes. Vous ne croyez pas ? Demandez lui "answer to life the universe and everything" (référence à l’oeuvre de Douglas Adams <em>H2G2, Le Guide du voyageur galactique</em>) !</p>

<h3>La définition </h3>
<p>Enfin une fonction vraiment pratique ! Vous vous êtes toujours demandés la signification de sycophante, thuriféraire, ou pandémonium ? Google.fr connait ! Il suffit de lui demander define:Mot. (Résultat : <cite class="comment">Definitions of sycophante on the Web in French: Un sycophante (en grec ancien / sukophántês) est, dans l'Athènes antique, un délateur professionnel.</cite>) </p>

<h3>L'heure <span class="comment">[Uniquement sur google.com]</span></h3>
<p><img src="Images/clock.png" alt="Horloge" />Google connait l'heure de n'importe quelle ville du monde ! Une requête what time is it renverra l'heure actuelle dans votre pays, tandis que what time is it Hong Kong donnera l'heure de Hong Kong. On ne sait jamais, ça peut servir !</p>

<h3>La recherche avancée de Google</h3>
<p>Sur la page d'accueil de Google se trouve un petit lien "Recherche Avancée". CLiquez dessus.<br />
De cette page vous pouvez régler toutes les options de recherche (la langue de recherche, la langue d'affichage des résultats, la date de dernière modification, l'extension du site - .fr, .com, .eu...). Pas forcément utile tous les jours, mais à retenir !</p>

<h3>Le proxy gratuit</h3>
<p>Votre entreprise vous a bloqué l'accès à une page Web dont vous avez besoin (pour votre travail évidemment !). Vous pouvez, tout de même, accéder à cette page : il suffit de cliquer sur le petit lien "en cache" qui s'affiche en bas de chaque réponse à une recherche. La page telle qu’elle a été enregistrée par "GoogleBot" lors de sa dernière visite s’affiche alors. Attention, cependant, le cache Google peut parfois avoir quelques jours de différé et les images restent hébergées sur le site principal : il est fort probables qu'elle restent bloquées.</p>

<h3>Élargir la recherche aux synonymes <span class="comment">(ne fonctionne pas très bien en français)</span></h3>
<p>En utilisant un ~ devant un mot, vous incluez aussi ses synonymes dans la recherche. Ainsi, '~nutrition' vous permettra d'avoir des pages contenant food, health... ans les résultats.</p>

<h3>La traduction</h3>
<p><img src="Images/Drapeau.png" alt="Horloge" />Google Traduction est un service très utile pour traduire rapidement une page Web. Il suffit de rentrer l'adresse du site, la langue dans laquelle il est rédigé et la langue dans laquelle vous désirez la lire. Il est aussi possible d'entrer directement un texte. Attention, cependant, n'espérez pas écrire votre dissertation d'anglais en français et la traduire via Google : si le résultat est compréhensible, il est très loin d'être écrit dans un langage sémantiquement correct... et votre professeur risque de ne pas apprécier. En revanche, Google est une alternative convenable à un dictionnaire Francais/Anglais ou Francais/Allemand.</p>

<h3>La recherche de musique</h3>
<p>Cette fonction est assez complexe, voici la phrase magique à indiquer :
<cite>-inurl:(htm|html|php) intitle:"index of" +"last modified" +"parent directory" +description +size +(wma|mp3) "Mozart"</cite><br />
Il ne faut surtout pas oublier le petit "-" au début, sinon la recherche ne renverra pas ce qui est cherché. Bien évidemment, il faut remplacer "Mozart" par l'artiste désiré... et n'oubliez pas, ne téléchargez que des musiques libres de droit !</p>

<h3>La recherche de BD</h3>
<p>L’astuce précédente fonctionne aussi pour trouver des BD :<br />
<cite>-inurl:htm -inurl:html intitle:"index of" "Last modified" simpsons cbr</cite><br />
Cette fois, on recherche les Simpsons… </p>

<h3>Recherche Faciale</h3>
<p>Enfin, si vous recherchez des personnes sur Google Images, vous pouvez exclure les objets pour n'afficher que des images de personnes. Ainsi, la requête "Rose" affiche normalement les fleurs, rajoutez <cite>"&amp;imgtype=face"</cite> dans l'URL, et miracle ! Seules les images de personnes sont présentes. Un exemple concret pourrait être la recherche d'image de Paris Hilton : l'ajout du paramètre imgtype=face fera disparaître toutes les photos de la ville de Paris. </p>

